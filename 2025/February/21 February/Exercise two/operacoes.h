#ifndef OPERACOES_H
#define OPERACOES_H

// Function to add two numbers
int soma(int a, int b) {
    return a + b;
}

// Function to subtract two numbers
int subtracao(int a, int b) {
    return a - b;
}

// Function to multiply two numbers
int multiplicacao(int a, int b) {
    return a * b;
}

#endif 